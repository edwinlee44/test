select  
GROUPS.M_LABEL as GROUP_LABEL, 
case(GROUPS.M_TYPE) 
when 0 then 'MX USER ADMNISTRATOR'
when 1 then 'MX LICENSE ADMNISTRATOR'
when 2 then 'MX right ADMNISTRATOR'
when 3 then 'MX MIDDLEWARE ADMNISTRATOR'
when 4 then 'MX INSTALL'
when 5 then 'MX GROUP'
when 6 then 'MLC GROUP'
when 7 then 'MLC right ADMNISTRATOR' end as GROUP_CATEGORY,
GRPD.M_DESC as GROUP_DESCRIPTION,
case (GRPD.M_BO_FO)
when 0 then 'FRONT OFFICE GROUP'
when 1 then 'P&L CONTROL GROUP'
when 2 then 'PROCESSING GROUP' end as GROUP_TYPE, 
GRPD.M_TRN_OFFSET as AREA_CODE, 
case(GRPD.M_DATE_MODE)
when 0 then 'DD/MM/YY'
when 1 then 'MM/DD/YY'
when 2 then 'YY/MM/DD'
else 'Default' end as DATE_DISPLAY,
case (bitand(GRPD.M_RGT_DEVICE,2)) when 2 then 'X' end as DEVICE_ACCESS_RIGHT,
case (bitand(GRPD.M_RGT_DEVICE,4)) when 4 then 'X' end as DEVICE_INSERT_RIGHT,
case (bitand(GRPD.M_RGT_DEVICE,8)) when 8 then 'X' end as DEVICE_SUPPRESS_RIGHT,
case (bitand(GRPD.M_RGT_DEVICE,16)) when 16 then 'X' end as DEVICE_MODIFY_RIGHT,
GRPD.M_CUT_OFF as DEFAULT_CUT_OFF,
A.M_LABEL as REPORTING_TEMPLATE_LABEL,
CWT.M_LABEL as CHINESE_WALL_TEMPLATE,
GRPD.M_DAP_TMPL as DATA_PUBLISHER_TEMPLATE,
WFALL.M_LABEL as OSP_RIGHT_TEMPLATE,
CONSHDR.M_LABEL as DATA_CONSISTENCY_TEMPLATE,
NAV.M_LABEL as NAVIGATION_TEMPLATE,
EM.M_LABEL as PL_CONTROL_TEMPLATE,
MUB.M_LABEL as PORTFOLIO_TREE_TEMPLATE,
ROL.M_LABEL as ROLE_TYPE,
case (GRPD.M_ACTIVITY)
when 'C' then 'X' end as CONFIGURATOR,
GRPD.M_DST_PROF as DISTRIBUTION_PROFILE,
PTF_ATMP.M_LABEL as PORTFOLIO_ALIAS_TEMPLATE,
CWT_IRS.M_LABEL as CWT_IRS_TEMPLATE,
CWT_LD.M_LABEL as CWT_LD_TEMPLATE,
CWT_CD.M_LABEL as CWT_CD_TEMPLATE,
GRP_STP.M_STP_RIGHTS as STP_RIGHTS_LABEL,
EFF_R.M_LABEL as HEDGE_EFFECTIVNESS_TEMPLATE,
STRAT_R.M_LABEL as STRATEGY_CODE_TEMPLATE,
STAT.M_LABEL as STATEMENT_CATEGORY_TEMPLATE,
TREE_R.M_LABEL as HIERARCHY_RIGHT_TEMPLATE,
JOIN_R.M_LABEL as JOIN_TEMPLATE,
TRN_EST.M_LABEL as ENTITY_SET_RIGHTS,
case (GRPD.M_IMP_PAST)
when 1 then 'X' end as ALLOW_IMPACT_PAST,
DSKD.M_LABEL as DESK_LABEL,
PC.M_LABEL as PROCESSING_CENTER_LABEL,
GRPD.M_TIMEZONE as TIME_ZONE,
TAB_UTLT.M_LABEL as USER_TABLE_LAYOUT 
FROM TRN_GRPD_DBF GRPD left outer join TABLE#STRUCT#TAB_UTLT_DBF TAB_UTLT on TAB_UTLT.M_REFERENCE = GRPD.M_UT_LAY_TPL left outer join TRN_PC_DBF PC on PC.M_REFERENCE = GRPD.M_PC left outer join TRN_DSKDL_DBF DSKDL on (DSKDL.M_CTN = GRPD.M_FODS and DSKDL.M_REF <> 0) left outer join TRN_DSKD_DBF DSKD on DSKD.M_REFERENCE= DSKDL.M_REF left outer join TRN_EST_DBF TRN_EST on TRN_EST.M_REFERENCE = GRPD.M_ENTSET_TPL left outer join CSF_JOIN_RGTH_DBF JOIN_R on JOIN_R.M_REFERENCE = GRPD.M_JOIN_RIGHT left outer join CSF_TREE_RGTH_DBF TREE_R on TREE_R.M_REFERENCE = GRPD.M_TREE_RIGHT left outer join HEDGE#STAT_TMPL_DBF STAT on STAT.M_REFERENCE = GRPD.M_STAT_TPL left outer join HEDGE#STRAT_TMPL_DBF STRAT_R on STRAT_R.M_REFERENCE = GRPD.M_STRCDE_TPL left outer join HEDGE#EFF_TMPL_DBF EFF_R on EFF_R.M_REFERENCE = GRPD.M_EFF_RUL left outer join GRP_STP_DBF GRP_STP on GRP_STP.M_GRP_REF = GRPD.M_REFERENCE left outer join CWT_RTGEN_LD_DBF CWT_LD on CWT_LD.M_REFERENCE=GRPD.M_CWT_LD left outer join CWT_RTGEN_CD_DBF CWT_CD on CWT_CD.M_REFERENCE=GRPD.M_CWT_CD left outer join CWT_RTGEN_IRS_DBF CWT_IRS on CWT_IRS.M_REFERENCE=GRPD.M_CWT_IRS left outer join PTF_ATMP_DBF PTF_ATMP on PTF_ATMP.M_REF = GRPD.M_PTF_ATMPL left outer join ROLE_DBF ROL on ROL.M_REFERENCE=M_ROLE left outer join MUB#MUB_TPL_DBF MUB on MUB.M_REFERENCE = GRPD.M_MUB_TMPL left outer join EM_CFGT_DBF EM on EM.M_REFERENCE = GRPD.M_PLC_TMPL left outer join NAV_TMPL_DBF NAV on NAV.M_REFERENCE = GRPD.M_NAV_TMPL left outer join CONS_HDR_DBF CONSHDR on GRPD.M_CONS = CONSHDR.M_REFERENCE left outer join WF_ALL_DBF WFALL on GRPD.M_STP_ALLOC = WFALL.M_LABEL left outer join CTP_CWT_DBF CWT on GRPD.M_CTP_CWT = CWT.M_REFERENCE left outer join ACT_RGTTMP_DBF A on GRPD.M_REP_TMPL = A.M_LABEL left outer join MX_GROUP_DBF GROUPS on ltrim(trim(GRPD.M_LABEL)) = GROUPS.M_LABEL
WHERE GROUPS.M_LABEL = @GroupLabel:C
order by GROUP_LABEL, GROUP_CATEGORY 