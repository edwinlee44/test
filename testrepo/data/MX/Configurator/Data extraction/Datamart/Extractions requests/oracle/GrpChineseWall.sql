select
GRP.M_LABEL as GROUP_LABEL,

CWT.M_LABEL as CWT_LABEL,

CTP.M_DSP_LABEL as COUNTERPART_LABEL
from TRN_GRPD_DBF GRP, MEN#MEN_BO_DBF MEN, CTP_CWT_DBF CWT, TRN_CPDF_DBF CTP

where

to_char(GRP.M_CTP_CWT )=trim(MEN.M_ENTITY)
and GRP.M_CTP_CWT=CWT.M_REFERENCE 
and MEN.M_LABEL=CTP.M_LABEL
and GRP.M_LABEL=@GroupLabel:C