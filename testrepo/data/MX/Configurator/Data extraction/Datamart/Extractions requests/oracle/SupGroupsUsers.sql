select  
GROUPS.M_LABEL as GROUP_LABEL, 
USERS.M_LABEL as USER_LABEL, 
USERS.M_DESC as USER_DESCRIPTION
FROM TRN_GRPD_DBF GRPD right outer join MX_GROUP_DBF GROUPS on GRPD.M_LABEL = GROUPS.M_LABEL left outer join MX_USER_GROUP_DBF USER_G  on GROUPS.M_REFERENCE = USER_G.M_GROUP_ID left outer join MX_USER_DBF USERS on USER_G.M_USER_ID = USERS.M_REFERENCE
WHERE    GROUPS.M_LABEL = @GroupLabel:C
order by GROUP_LABEL, USER_LABEL  