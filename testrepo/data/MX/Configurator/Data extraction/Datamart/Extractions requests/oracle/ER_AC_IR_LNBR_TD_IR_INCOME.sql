SELECT T1.M_NB,T1.M_ACCOUNT ACCOUNT_LABEL,T1.M_EN_AMT FTDTOT,   
       T1.M_EN_CCY FCCY,T1.M_TP_PFOLIO FPFOLIO,
       T1.M_BU_CD BU_FLAG, T1.M_EN_DATE DATA_DT,
       --T1.M_D_C, 
       REGEXP_SUBSTR(T1.M_EN_COMMENT,'[^;]+',1,2) M_LABEL,
       CTP.M_NAME
FROM mx_acg_entry_rep T1
LEFT JOIN V_EOD_DATE EOD ON 1=1 
LEFT JOIN mx_counterp_rep CTP ON TRIM(CTP.M_DSP_LABEL) = REGEXP_SUBSTR(T1.M_EN_COMMENT,'[^;]+',1,2)
WHERE M_ACCOUNT IN ('4501-NOST-TD-BK', '1151-NOST-TD-BK')
  AND T1.M_EN_DATE BETWEEN  EOD.RPT_DT_LM_FST AND  EOD.RPT_DT_LM_LST

ORDER BY M_D_C DESC,M_EN_DATE, M_NB