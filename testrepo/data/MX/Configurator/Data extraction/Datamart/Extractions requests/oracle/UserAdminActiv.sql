select 
AUDIT1.M_CONN_ID as CONNECTION_ID, 
case AUDIT1.M_LOG_TYPE when 'USER_POLICY_CHANGE' then 'USER_POLICY_CHANGE - User: ' || AUDIT1.M_ENTITY 
when 'REMOVE_USER_FROM_GROUP' then 'REMOVE_USER_FROM_GROUP - User: ' || AUDIT1.M_ENTITY
when 'USER_DESCRIPTION_MODIFIED' then 'USER_DESCRIPTION_MODIFIED - User: ' || AUDIT1.M_ENTITY
when 'USER_CODE_MODIFIED' then 'USER_CODE_MODIFIED - User: ' || AUDIT1.M_ENTITY
when 'CHANGE_PASSWORD' then 'CHANGE_PASSWORD - User: ' || AUDIT1.M_ENTITY
when 'ADD_USER_TO_GROUP' then 'ADD_USER_TO_GROUP - User: ' || AUDIT1.M_ENTITY
when 'RESET_PASSWORD' then 'RESET_PASSWORD - User: ' || AUDIT1.M_ENTITY
when 'ADD_USER' then 'ADD_USER - User: ' || AUDIT1.M_ENTITY
when 'ADD_GROUP' then 'ADD_GROUP - Group: ' || AUDIT1.M_ENTITY
when 'REMOVE_GROUP' then 'REMOVE_GROUP - Group: ' || AUDIT1.M_ENTITY
else AUDIT1.M_LOG_TYPE || ' : ' || AUDIT1.M_ENTITY
end as EVENT_ACTION, 
to_char(AUDIT1.M_LOG_DATE, 'MON DD YYYY HH:MI:SS')  as COMPUTER_DATE, 
case AUDIT1.M_COL_NAME when 'M_PASSWORD' then '*******' else AUDIT1.M_OLD_VAL end as OLD_VALUE, 
case AUDIT1.M_COL_NAME when 'M_PASSWORD' then '*******' else AUDIT1.M_NEW_VAL end as NEW_VALUE, 
CONNECTION1.M_USER_NAME as USER_LABEL, 
AUDIT1.M_COL_NAME as MODIFIED_FIELD

from 
MX_USER_ADMIN_AUDIT_DBF AUDIT1, 
MX_USER_CONNECTION_DBF CONNECTION1 

where 
AUDIT1.M_CONN_ID = CONNECTION1.M_REFERENCE 
and AUDIT1.M_LOG_DATE >= @FirstDate:D and AUDIT1.M_LOG_DATE <=@SecondDate:D